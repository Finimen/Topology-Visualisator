using UnityEngine;
using UnityEngine.UI;

namespace Robots.MainCore.UI
{
    [DisallowMultipleComponent]
    internal class UIElement : MonoBehaviour
    {
        [SerializeField] private Transform hiddenTransform;
        [SerializeField] private Transform showenTransform;

        private bool active;

        public void ShowUI(float timeScale)
        {
            LeanTween.move(gameObject, showenTransform.position, timeScale);
        }

        public void HideUI(float timeScale)
        {
            LeanTween.move(gameObject, hiddenTransform.position, timeScale);
        }
    }
}